package com.ccps.exchgate.api.pojo;

import java.io.Serializable;

/**
 * @author fandong17032
 *	调用运行请求参数类
 */
public class RunReqParam implements Serializable {

	private static final long serialVersionUID = -6053518725094216654L;
	private String exchangeId;
	private String exchangeMarketType;
	private String bizType;
	private String initDate;
	private String exchangeFlag;
	private String nextWorkday;
	public String getExchangeId() {
		return exchangeId;
	}
	public void setExchangeID(String exchangeId) {
		this.exchangeId = exchangeId;
	}
	public String getExchangeMarketType() {
		return exchangeMarketType;
	}
	public void setExchangeMarketType(String exchangeMarketType) {
		this.exchangeMarketType = exchangeMarketType;
	}
	public String getBizType() {
		return bizType;
	}
	public void setBizType(String bizType) {
		this.bizType = bizType;
	}
	public String getInitDate() {
		return initDate;
	}
	public void setInitDate(String initDate) {
		this.initDate = initDate;
	}
	public String getExchangeFlag() {
		return exchangeFlag;
	}
	public void setExchangeFlag(String exchangeFlag) {
		this.exchangeFlag = exchangeFlag;
	}
	public String getNextWorkday() {
		return nextWorkday;
	}
	public void setNextWorkday(String nextWorkday) {
		this.nextWorkday = nextWorkday;
	}
	
}
